/**
 * Returns the municipality name for a Spanish postal code.
 * Priority: exact 5-digit > province fallback (2-digit)
 */
export function lookupPostalCode(postalCode: string): string {
  if (!postalCode || postalCode.length < 2) return '';
  
  // Try exact 5-digit match
  if (postalCode.length === 5 && POSTAL_5[postalCode]) {
    return POSTAL_5[postalCode];
  }
  
  // Fallback to province capital
  const prefix2 = postalCode.slice(0, 2);
  return PROVINCE_MAP[prefix2] || '';
}
